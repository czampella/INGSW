    private void selectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectButtonActionPerformed
        s=ShipmentEntity.getShipmentEntity();
        
        switch(stato){
            case("client"):{
                c=ClientEntity.getClientEntity();
        
                c.setCf(tableList.getValueAt(tableList.getSelectedRow(), 0).toString());
                c.setName(tableList.getValueAt(tableList.getSelectedRow(), 1).toString());
                c.setSurname(tableList.getValueAt(tableList.getSelectedRow(), 2).toString());
                c.setAddress(tableList.getValueAt(tableList.getSelectedRow(), 3).toString());
                c.setCap(tableList.getValueAt(tableList.getSelectedRow(), 4).toString());
                c.setTelephone(tableList.getValueAt(tableList.getSelectedRow(), 5).toString());
                c.setCity(tableList.getValueAt(tableList.getSelectedRow(), 6).toString());
                s.setIdClient(c.getCf());
                Assignments.fillForm(c);
                break;
            }
            case("driver"):{
                String st=tableList.getValueAt(tableList.getSelectedRow(), 0).toString();
                Assignments.driverField.setText(st);
                s.setIdDriver(st);
                break;
            }
            case("vehicle"):{
                String st=tableList.getValueAt(tableList.getSelectedRow(), 0).toString();
                Assignments.vehicleField.setText(st);
                s.setIdVehicle(st);
                break;
            }
            
        }
        //as = new Assignments();
        //as.setVisible(true);
        this.dispose();        
    }//GEN-LAST:event_selectButtonActionPerformed

